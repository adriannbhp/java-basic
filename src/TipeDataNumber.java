public class TipeDataNumber {
    public static void main(String[] args) {

        /* Integer Number */

        byte iniByte = 100;  // Size = 1 byte
        short iniShort = 1000; // Size = 2 bytes
        int iniInt = 100000000; // Size = 4 bytes
        long iniLong = 1000000000; // Size = 8 bytes
        long iniLong2 = 1000000000L; // Size = 8 bytes

        /* Floating Point Number */

        float iniFloat = 10.12F; // Size = 4 bytes
        double iniDouble = 10.12; // Size = 8 bytes

        /* Literals */
        int decimalInt = 34;
        int hexaDecimal = 0xFFFFF;
        int binaryDecimal = 0b10101010;

        /* Underscore */

        long balance = 1_000_000_000_000L;
        int sum = 60_000_000  ;

    }

}
